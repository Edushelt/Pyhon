def Gender_census(Male_count, Female_count):
    print(f"We have {Male_count} number of Male at this section")
    print(f"We have {Female_count} number of Female at this section")
    print(f"Total: {Male_count} + {Female_count} equals {Male_count + Female_count}\n")
    
Gender_census(20, 15)
Gender_census(36, 50)
